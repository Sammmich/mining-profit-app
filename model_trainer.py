def get_sample_df():
    """Возвращает DataFrame из встроенной выборки."""
    return pd.read_csv(io.StringIO(SAMPLE_CSV.strip()))


def train_and_save(save_path):
    """
    Обучает модель на встроенной выборке и сохраняет в save_path.
    save_path — путь к файлу .joblib (например /tmp/profit_model.joblib на облаке).
    """
    df = get_sample_df()
    X = df[NUMERIC_FEATURES + [CAT_FEATURE]].copy()
    y = df[TARGET_COL]

    try:
        ohe = OneHotEncoder(drop="first", sparse_output=False)
    except TypeError:
        ohe = OneHotEncoder(drop="first", sparse=False)

    preprocessor = ColumnTransformer(
        transformers=[
            ("num", StandardScaler(), NUMERIC_FEATURES),
            ("cat", ohe, [CAT_FEATURE]),
        ],
        remainder="passthrough",
    )
    pipeline = Pipeline([
        ("preprocessor", preprocessor),
        ("regressor", LinearRegression()),
    ])
    pipeline.fit(X, y)

    save_path = Path(save_path)
    save_path.parent.mkdir(parents=True, exist_ok=True)
    joblib.dump({
        "pipeline": pipeline,
        "numeric_features": NUMERIC_FEATURES,
        "cat_feature": CAT_FEATURE,
        "target_col": TARGET_COL,
    }, save_path)
    return save_path
